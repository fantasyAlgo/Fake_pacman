void loadMap(std::string mapFile){
  std::ifstream file(mapFile);
  std::string line;
  int i = 0;
  while (getline(file, line)){
    //std::cout << line << std:
    for (int j = 0;  j < MAP_WIDTH; j++) {
      pacman_map[i][j] = line[j]-48;
    }
    i++;
  }
  file.close();
}

